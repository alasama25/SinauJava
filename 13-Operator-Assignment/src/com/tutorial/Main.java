package com.tutorial;

public class Main {

    public static void main(String[] args) {

        // Operator Assignment

        // Assignment operator jumlah
        int a = 1;
        a += 10; // a = a + 10;
        System.out.printf("nilai a = %d\n", a);

        // Assignment operator pengurangan
        int b = 100;
        b -= 25; // a = a + 10;
        System.out.printf("nilai b = %d\n", b);

        // Assignment operator perkalian
        int c = 100;
        c *= 20; // a = a + 10;
        System.out.printf("nilai c = %d\n", c);

        // Assignment operator pembagian
        int d = 100;
        d /= 20; // a = a + 10;
        System.out.printf("nilai d = %d\n", d);

        // Assignment operator modulus
        int e = 10;
        e %= 7; // a = a + 10;
        System.out.printf("nilai e = %d\n", e);


    }

}
